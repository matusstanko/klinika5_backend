app.post("https://klinika5backend-production.up.railway.app/api/cancel_reservation", async (req, res) => {
    const { cancellation_token } = req.body;

    if (!cancellation_token) {
        return res.status(400).json({ error: "Ch√Ωba storno token!" });
    }

    const client = await pool.connect();

    try {
        await client.query("BEGIN");

        // 1Ô∏è‚É£ Find the reservation by `cancellation_token`
        const reservationResult = await client.query(
            "SELECT id, time_slot_id, email, phone FROM reservations WHERE cancellation_token = $1",
            [cancellation_token]
        );

        if (reservationResult.rows.length === 0) {
            await client.query("ROLLBACK");
            return res.status(404).json({ error: "Rezerv√°cia neexistuje alebo u≈æ bola zru≈°en√°." });
        }

        const { id, time_slot_id, email, phone } = reservationResult.rows[0];

        // 2Ô∏è‚É£ Get reservation date and time
        const timeSlotResult = await client.query(
            "SELECT date, time FROM time_slots WHERE id = $1",
            [time_slot_id]
        );

        if (timeSlotResult.rows.length === 0) {
            await client.query("ROLLBACK");
            return res.status(500).json({ error: "Chyba pri z√≠skavan√≠ inform√°ci√≠ o term√≠ne." });
        }

        // ‚úÖ Fix: Ensure correct handling of date and time
        const rawDate = new Date(timeSlotResult.rows[0].date); // Ensure it's a Date object
        const rawTime = String(timeSlotResult.rows[0].time); // Ensure it's a string

        const { formattedDate, formattedTime } = formatDateTime(rawDate, rawTime);

        // 3Ô∏è‚É£ Delete the reservation
        await client.query("DELETE FROM reservations WHERE id = $1", [id]);

        // 4Ô∏è‚É£ Free up the time slot (`is_taken = false`)
        await client.query("UPDATE time_slots SET is_taken = false WHERE id = $1", [time_slot_id]);

        await client.query("COMMIT");

        console.log(`‚úÖ Rezerv√°cia ID ${id} bola √∫spe≈°ne zru≈°en√°.`);

        // 5Ô∏è‚É£ Send cancellation email
        sendCancelEmail(email, phone, { formattedDate, formattedTime });

        // 6Ô∏è‚É£ Send cancellation SMS
        const newBookingLink = `http://localhost:3000/objednat-sa.html`;
        const cancellationMessage = `‚ùå Va≈°a rezerv√°cia bola zru≈°en√°.\nüìÖ D√°tum: ${formattedDate}\n‚è∞ ƒåas: ${formattedTime}\nüîÑ Nov√° rezerv√°cia: ${newBookingLink}`;
        sendSMS(phone, cancellationMessage);

        res.json({ message: "Rezerv√°cia bola √∫spe≈°ne zru≈°en√° a term√≠n je op√§≈• dostupn√Ω." });

    } catch (err) {
        await client.query("ROLLBACK");
        console.error("‚ùå Chyba pri ru≈°en√≠ rezerv√°cie:", err);
        res.status(500).json({ error: "Chyba pri ru≈°en√≠ rezerv√°cie." });
    } finally {
        client.release();
    }
});




























